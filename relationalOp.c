// Example of Relational operators 
#include<stdio.h>
int main()
{
	int num1,num2;
	printf("Please enter 2 numbers ");
	scanf("%d%d",&num1,&num2);
	
	printf("Number1 = %d Number2 = %d",num1,num2);
	
	printf("\n %d==%d    %d ",num1,num2,num1==num2);
	printf("\n != %d ",num1!=num2);
	printf("\n > %d ",num1>num2);
	printf("\n < %d ",num1<num2);
	printf("\n >= %d ",num1>=num2);
	printf("\n <= %d ",num1<=num2);
}
	
	
	
	
	
